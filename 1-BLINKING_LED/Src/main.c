  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(T3_BASE_GPIO_Port, T3_BASE_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOB, T3_EMITTER_Pin|LED_BLUE_Pin|LED_RED_Pin|LED_GREEN_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pin : T3_BASE_Pin */
